#accessing elements from a tuple
#elements in tuple are access by round brackets
my_tuple =("c","c++","java","python")
print(my_tuple)
#output will be ('c','c++','java','python')
#the length can be access in the tuple
print(len(my_tuple))
#output will be 4
#it can have index-based access
print(my_tuple[1])
#output will be c++
